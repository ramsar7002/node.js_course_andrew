const Task = mongoose.model("Task", {
  description: {
    type: String,
    required: true,
    trim: true,
  },
  completed: {
    type: Boolean,
    default: false,
  },
});

const myTask = new Task({ description: "Go to the gym", completed: true });

myTask
  .save()
  .then((res) => console.log(res))
  .catch((err) => console.log(err));
